def get_shared_stops(bus_stops):
    merged_bus_stops = sorted(list(itertools.chain(*bus_stops.values())))
    result = []
    for key, group in itertools.groupby(merged_bus_stops):
        # if you print, no return, because iterator is exhausted
        # print(key, list(group))  
        if len(list(group)) > 1:
            result.append(key)
    
    return result

